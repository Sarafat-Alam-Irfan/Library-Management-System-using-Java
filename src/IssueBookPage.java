    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String studentID = _studentID.getText();
        String bookID = _bookID.getText();
        
        String dburl="jdbc:mysql://localhost:3306/librarydb";
        String dbusername="root";
        String dbpassword="admin";
        String dbquery="SELECT * FROM BOOKS WHERE ID='"+bookID+"';";
        try{
            Connection conn = DriverManager.getConnection(dburl, dbusername, dbpassword);
            Statement statement = conn.createStatement();
            ResultSet resultSet = statement.executeQuery(dbquery);
            if(resultSet.next()) {
                _name.setText(resultSet.getString("NAME"));
                _author.setText(resultSet.getString("AUTHOR"));
                _year.setText(resultSet.getString("YEAR"));
                _stock.setText(resultSet.getString("STOCK"));
                
                DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                Date currentDate = new Date();
                _issueDate.setText(dateFormat.format(currentDate));

                Calendar c = Calendar.getInstance();
                c.setTime(currentDate);

                c.add(Calendar.DATE, 7);
                Date currentDatePlusSeven = c.getTime();

                _returnDate.setText(dateFormat.format(currentDatePlusSeven));
                
            } else {
                JOptionPane.showMessageDialog(this, "No books found.");
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        dbquery="SELECT * FROM STUDENT WHERE ID='"+studentID+"';";
        try{
            Connection conn = DriverManager.getConnection(dburl, dbusername, dbpassword);
            Statement statement = conn.createStatement();
            ResultSet resultSet = statement.executeQuery(dbquery);
            if(resultSet.next()) {
                _studentName.setText(resultSet.getString("NAME"));
                
            } else {
                JOptionPane.showMessageDialog(this, "No students found.");
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

