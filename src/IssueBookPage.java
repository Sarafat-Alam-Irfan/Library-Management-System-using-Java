        String returnDate = _returnDate.getText();
        
        String dburl="jdbc:mysql://localhost:3306/librarydb";
        String dbusername="root";
        String dbpassword="admin";
        String dbquery="INSERT INTO ISSUENCE(BOOK_ID,STUDENT_ID,ISSUEDATE,RETURNDATE,STATUS) VALUES('"+bookID+"','"+studentID+"','"+issueDate+"','"+returnDate+"', 'PENDING');";
